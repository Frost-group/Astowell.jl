	    steer_rate = 1.0f-1, # add random noise to end of the time span
	    λ₁ = 1.0f-2, # regulate flow
	    λ₂ = 1.0f-2, # regulate volume change
	    λ₃ = 1.0f-2, # regulate augmented dimensions
	)
	
end

