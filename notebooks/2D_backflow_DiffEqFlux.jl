"""Build a two dimensional reciprocal space. The 2008 PRB chooses N=49 as it makes a nice regular KMAX<=4 space in 2D."""
function K(;N=49)
	k=zeros(N,2) # kludge static allocation
	n=1

	for kx ∈ -KMAX:KMAX
		for ky ∈ -KMAX:KMAX
#			print(kx," ",ky,"\n")
			if kx^2+ky^2<=KMAX^2
				k[n,1]=kx
				k[n,2]=ky
				n=n+1
			end
		end
	end
	
	return k
end


